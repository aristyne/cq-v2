export type BaseLevel = {
  id: number;
  topicId: number;
  topicTitle: string;
  isFirstInTopic: boolean;
  title: string;
  description: string;
  challenge: string;
  xp: number;
  difficulty: 'Easy' | 'Medium' | 'Hard';
  hints: string[];
};

export type CodeLevel = BaseLevel & {
  type: 'code';
  starterCode: string;
  solution: string;
  expectedOutput: string;
};

export type MultipleChoiceLevel = BaseLevel & {
  type: 'multiple-choice';
  choices: string[];
  correctAnswer: number;
  solution: string; // Explanation for the correct answer
};

